package com.example.newidentity;

public class Birthday {

    private String dmy;
    private String mdy;
    private int raw;

    public Birthday() {
    }

    public String getDmy() {
        return dmy;
    }

    public void setDmy(String dmy) {
        this.dmy = dmy;
    }

    public String getMdy() {
        return mdy;
    }

    public void setMdy(String mdy) {
        this.mdy = mdy;
    }

    public int getRaw() {
        return raw;
    }

    public void setRaw(int raw) {
        this.raw = raw;
    }

}
